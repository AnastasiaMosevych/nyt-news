export function getPopularNews() {
  const result = popular.results.map(
    (
      {
        uri,
        url,
        section,
        nytdsection,
        published_date,
        abstract,
        title,
        media,
      },
      index
    ) => {
      //console.log(`index=${index}-`, media[0]['media-metadata'][2].url);
      const result = {
        uri,
        url,
        sectionName: section,
        section: nytdsection,
        newsDate: published_date,
        snippet: abstract,
        title,
        isFavorite: false,
        isRead: false,
      };
      if (media.length === 0) result.image = noImageURL;
      else result.image = media[0]['media-metadata'][2].url;
      return result;
    }
  );
  return result;
}
